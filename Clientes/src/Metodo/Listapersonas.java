    /* aqui se convierte el array en una matriz y se le asignan los atributos de los objetos en la fila 
     y la columna a la que corresponden de tal manera que la tabla quede organizada*/

    private void ajustarDatos(Persona[] personas) {
        pers = new Object[personas.length][7];
        for (int i = 0; i < personas.length; i++) {
            System.out.println("pers: " + personas[i]);
            pers[i][0] = personas[i].tipodoc;
            pers[i][1] = personas[i].ndoc;
            pers[i][2] = personas[i].noms;
            pers[i][3] = personas[i].apells;
            pers[i][4] = personas[i].email;
            pers[i][5] = personas[i].ncel;
            pers[i][6] = personas[i].fnaci;
        }
    }

