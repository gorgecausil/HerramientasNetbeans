    private void EliminarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EliminarButtonActionPerformed
        /* se hace esto para poder eliminar la fila seleccionada en la tabla*/
        int fila = tbPersonas.getSelectedRow();
        if (fila >= 0) {
            ((DefaultTableModel) tbPersonas.getModel()).removeRow(tbPersonas.getSelectedRow());
        }
    }//GEN-LAST:event_EliminarButtonActionPerformed

    private void ButtonModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonModificarActionPerformed
        /* aqui se selecciona la tabla y se almacena en la variable que guarda ene el array que posteriomente
         se guarda en el archivo de texto*/
        int count = ((DefaultTableModel) tbPersonas.getModel()).getRowCount();
        ventanaPrincipal.pers = new Persona[count];
        for (int i = 0; i < count; i++) {
            Persona perTmp = new Persona();
            perTmp.tipodoc = "" + ((DefaultTableModel) tbPersonas.getModel()).getValueAt(i, 0);
            perTmp.ndoc = "" + ((DefaultTableModel) tbPersonas.getModel()).getValueAt(i, 1);
            perTmp.noms = "" + ((DefaultTableModel) tbPersonas.getModel()).getValueAt(i, 2);
            perTmp.apells = "" + ((DefaultTableModel) tbPersonas.getModel()).getValueAt(i, 3);
            perTmp.email = "" + ((DefaultTableModel) tbPersonas.getModel()).getValueAt(i, 4);
            perTmp.ncel = "" + ((DefaultTableModel) tbPersonas.getModel()).getValueAt(i, 5);
            perTmp.fnaci = "" + ((DefaultTableModel) tbPersonas.getModel()).getValueAt(i, 6);

            ventanaPrincipal.pers[i] = perTmp;
        }
        setVisible(false);
    }//GEN-LAST:event_ButtonModificarActionPerformed

